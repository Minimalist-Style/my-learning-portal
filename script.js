let clickCount = 0;

// –ù–∞—Ö–æ–¥–∏–º —ç–ª–µ–º–µ–Ω—Ç—ã –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ –ø–æ –∏—Ö ID
// document.getElementById() - –∏—â–µ—Ç —ç–ª–µ–º–µ–Ω—Ç —Å —É–∫–∞–∑–∞–Ω–Ω—ã–º id
const button = document.getElementById('clickButton');
const countDisplay = document.getElementById('count');

// –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ —Å–æ–±—ã—Ç–∏—è "click" –Ω–∞ –∫–Ω–æ–ø–∫—É
// –ö–æ–≥–¥–∞ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∫–ª–∏–∫–∞–µ—Ç - –≤—ã–ø–æ–ª–Ω—è–µ—Ç—Å—è —Ñ—É–Ω–∫—Ü–∏—è –≤–Ω—É—Ç—Ä–∏
button.addEventListener('click', function() {
    // –£–≤–µ–ª–∏—á–∏–≤–∞–µ–º —Å—á—ë—Ç—á–∏–∫ –Ω–∞ 1
    clickCount = clickCount + 1;
    
    // –ú–æ–∂–Ω–æ –∑–∞–ø–∏—Å–∞—Ç—å –∫–æ—Ä–æ—á–µ: clickCount++
    
    // –û–±–Ω–æ–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
    countDisplay.textContent = clickCount;
    
    // –î–æ–±–∞–≤–ª—è–µ–º –≤–∏–∑—É–∞–ª—å–Ω—ã–π —ç—Ñ—Ñ–µ–∫—Ç
    button.style.transform = 'scale(0.95)';
    
    // –ß–µ—Ä–µ–∑ 100 –º–∏–ª–ª–∏—Å–µ–∫—É–Ω–¥ –≤–æ–∑–≤—Ä–∞—â–∞–µ–º –≤ –∏—Å—Ö–æ–¥–Ω–æ–µ —Å–æ—Å—Ç–æ—è–Ω–∏–µ
    setTimeout(function() {
        button.style.transform = 'scale(1)';
    }, 100);
    
    // –ú–µ–Ω—è–µ–º —ç–º–æ–¥–∑–∏ –ø—Ä–∏ –æ–ø—Ä–µ–¥–µ–ª—ë–Ω–Ω–æ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–µ –∫–ª–∏–∫–æ–≤
    const avatar = document.querySelector('.avatar-emoji');
    
    if (clickCount >= 50) {
        avatar.textContent = 'üèÜ';
    } else if (clickCount >= 25) {
        avatar.textContent = '‚≠ê';
    } else if (clickCount >= 10) {
        avatar.textContent = 'üî•';
    } else if (clickCount >= 5) {
        avatar.textContent = 'üí™';
    }
    
    // –í—ã–≤–æ–¥–∏–º —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ –∫–æ–Ω—Å–æ–ª—å –±—Ä–∞—É–∑–µ—Ä–∞ (–¥–ª—è –æ—Ç–ª–∞–¥–∫–∏)
    console.log('–ö–æ–ª–∏—á–µ—Å—Ç–≤–æ –∫–ª–∏–∫–æ–≤:', clickCount);
});

